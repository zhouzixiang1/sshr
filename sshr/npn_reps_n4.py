"""
n=4 的 NPN 等价类代表元（222 个，含零函数）。

NPN 等价（Negation-Permutation-Negation Equivalence）
=====================================================
两个 n 变量布尔函数 f 和 g 称为 NPN 等价，若存在：
  - 输入变量置换 π ∈ S_n
  - 输入取反向量 c ∈ {0,1}^n
  - 输出取反位 b ∈ {0,1}
使得 g(x) = f(π(x) ⊕ c) ⊕ b。

n=4 共有 2^16 = 65536 个布尔函数，其 NPN 等价类数为 222。

代表元选取约定
==============
本文件的代表元采用"最优互补（optimal complement）"约定：
对每个 NPN 等价类，在 f 和 NOT(f) 中选取 SSHR-I（WP-SCP ILP，CNOT
目标）代价更小的那个。若两者代价相同，则保留原始代表元（f）。

这一约定与 Zheng et al.（2025）论文中 Table VI n=4 的测试集一致——
使用本列表跑 SSHR-I CNOT 目标可精确复现论文 CNOT=4696 的结果。

具体数字：
  - 222 个等价类中，131 个使用 f，90 个使用 NOT(f)，1 个为零函数
  - NOT(f) 的 truth table 满足：comp_tt = original_tt XOR 0xFFFF

历史背景
========
最初版本（NPN_REPS_N4_ORIGINAL）直接使用 BFS 枚举的 NPN 规范形式，
未考虑互补选择，导致 SSHR-I n=4 总 CNOT=5342（高于论文 4696 约 14%）。
根因分析表明：NPN 等价变换中的"输出取反"（output complementation）
会改变 WP-SCP ILP 的最优值；论文统一选取代价更低的代表元作为标准。

验证
====
>>> from sshr_i import sshr_i
>>> from bool_func import BooleanFunction, mct_cost
>>> def cnot(c): return sum(mct_cost(len(g.controls))['CNOT'] if g.type=='MCT'
...     else (1 if g.type=='CNOT' else 0) for g in c.gates)
>>> total = sum(cnot(sshr_i(BooleanFunction(4, tt), 'cnot', 30))
...     for tt in NPN_REPS_N4 if BooleanFunction(4, tt).onset)
>>> assert total == 4696, f"Expected 4696, got {total}"
"""

