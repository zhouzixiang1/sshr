# 辅助函数
# ---------------------------------------------------------------------------

def t_count(table: dict[int, tuple[int, int, int]], n: int) -> int:
    """取指定 n 的 T-count。"""
    return table[n][0]


def cnot_count(table: dict[int, tuple[int, int, int]], n: int) -> int:
    """取指定 n 的 CNOT 总数。"""
    return table[n][1]


def ancilla_count(table: dict[int, tuple[int, int, int]], n: int) -> int:
    """取指定 n 的 Ancillary 数。"""
    return table[n][2]


def cnot_gain_vs(our_cnot: int, baseline_table: dict[int, tuple[int, int, int]], n: int) -> float:
    """计算 CNOT 减少百分比: (baseline - ours) / baseline * 100"""
    baseline = cnot_count(baseline_table, n)
    return (baseline - our_cnot) / baseline * 100


