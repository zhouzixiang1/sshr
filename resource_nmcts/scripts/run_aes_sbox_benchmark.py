CONFIG = SearchConfig()
OUTPUT_DIR = Path(__file__).resolve().parent.parent / "results"

def main():
    rows = []
    for bit in range(8):
        tt = 0
        for x in range(256):
            if (AES_SBOX[x] >> bit) & 1:
                tt |= (1 << x)
        bf = BooleanFunction(8, tt)
        name = f'aes_sbox_bit{bit}'

        print(f"\n=== {name} ===", flush=True)

        # Direct ANF
        t0 = time.time()
        r_direct = synthesize('and_direct_anf', bf, CONFIG, seed=42)
        t_d = time.time() - t0
        s_d = r_direct.cost.score(CONFIG.weights)
        print(f"  Direct: score={s_d:.2f} T={r_direct.cost.T} CNOT={r_direct.cost.CNOT} time={t_d:.1f}s", flush=True)

        # Resource NMCTS with v2 prior
        t0 = time.time()
        r_resource = synthesize('and_resource_nmcts', bf, CONFIG, seed=42,
                                model_path='models/action_scorer_v2.pt')
        t_r = time.time() - t0
        s_r = r_resource.cost.score(CONFIG.weights)
        improve = (s_d - s_r) / max(s_d, 0.001) * 100
        print(f"  Resource: score={s_r:.2f} T={r_resource.cost.T} CNOT={r_resource.cost.CNOT} "
              f"improve={improve:.1f}% correct={r_resource.correct} time={t_r:.1f}s", flush=True)

        rows.append({
            'name': name, 'n': 8, 'bit': bit,
            'direct_score': round(s_d, 2), 'resource_score': round(s_r, 2),
            'direct_T': r_direct.cost.T, 'resource_T': r_resource.cost.T,
            'direct_CNOT': r_direct.cost.CNOT, 'resource_CNOT': r_resource.cost.CNOT,
            'direct_depth': r_direct.cost.depth, 'resource_depth': r_resource.cost.depth,
            'direct_ancilla': r_direct.cost.peak_ancilla,
            'resource_ancilla': r_resource.cost.peak_ancilla,
            'improvement_pct': round(improve, 2),
            'correct': r_resource.correct,
            'time_s': round(t_r, 2),
        })

    out_csv = OUTPUT_DIR / "raw_aes_sbox_benchmark.csv"
    with open(out_csv, 'w', newline='') as f:
        w = csv.DictWriter(f, fieldnames=rows[0].keys())
        w.writeheader()
        w.writerows(rows)

    mean_d = statistics.mean(r['direct_score'] for r in rows)
    mean_r = statistics.mean(r['resource_score'] for r in rows)
    mean_imp = statistics.mean(r['improvement_pct'] for r in rows)
    all_correct = all(r['correct'] for r in rows)
    print(f"\n=== AES S-box Summary ===")
    print(f"Mean direct score:   {mean_d:.2f}")
    print(f"Mean resource score: {mean_r:.2f}")
    print(f"Mean improvement:    {mean_imp:.2f}%")
    print(f"All correct:         {all_correct}")
    print(f"Wrote {len(rows)} rows to {out_csv}")

if __name__ == "__main__":
    main()
