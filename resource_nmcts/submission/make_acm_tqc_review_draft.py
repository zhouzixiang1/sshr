#!/usr/bin/env python3
"""Generate an ACM TQC review-format draft from the anonymous manuscript.

This is a formatting smoke path, not final venue metadata.  The generated
source keeps anonymous-review authorship and review-stage ACM metadata so the
selected venue can still drive final author fields, rights text, and archive
links.
"""
from __future__ import annotations

import re
import sys
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parent.parent
PAPER_DIR = PROJECT_ROOT / "paper_latex"
ANONYMOUS_SOURCE = PAPER_DIR / "resource_nmcts_submission_anonymous.tex"
ACM_SOURCE = PAPER_DIR / "resource_nmcts_submission_acm_tqc.tex"


ACM_PREAMBLE_TEMPLATE = r"""\documentclass[manuscript,screen,review,anonymous]{acmart}
% Generated by make_acm_tqc_review_draft.py; do not edit manually.
% This is an ACM/TQC formatting smoke draft. Final author metadata, rights text,
% DOI, ORCID, and archive links remain author/venue input.

\setcopyright{none}
\settopmatter{printacmref=true, printccs=true, printfolios=true}
\renewcommand\footnotetextcopyrightpermission[1]{}
\acmJournal{TQC}
\acmYear{2026}
\acmVolume{0}
\acmNumber{0}
\acmArticle{0}
\acmMonth{7}
\acmDOI{}

\usepackage{booktabs}
\usepackage{tabularx}
\usepackage{array}
\usepackage{float}
\usepackage{algorithm}
\usepackage{algpseudocode}
\usepackage{microtype}
\graphicspath{{figures/submission_v36/}}

\newcommand{\method}{Resource-NMCTS}
\newcommand{\anf}{\ensuremath{\mathrm{ANF}}}
\newcommand{\fprm}{\ensuremath{\mathrm{FPRM}}}
\newcommand{\mcts}{\ensuremath{\mathrm{MCTS}}}
\newcommand{\xor}{\ensuremath{\oplus}}
\newcommand{\mct}{\ensuremath{\mathrm{MCT}}}
\newcommand{\rz}{\ensuremath{R_z}}
\newcommand{\score}{\ensuremath{\mathrm{score}}}
\newcommand{\ttable}{truth table}

\title{{title}}
\author{Anonymous Authors}
\affiliation{%
  \institution{Anonymous Institution}
  \country{Anonymous Country}
}
\ccsdesc[500]{Theory of computation~Quantum computation theory}
\ccsdesc[300]{Hardware~Reversible logic}
\ccsdesc[300]{Computing methodologies~Search methodologies}
\keywords{quantum Boolean oracle synthesis, Monte Carlo tree search, reinforcement learning, contextual bandit, resource-aware synthesis, reversible logic, algebraic normal form}

\begin{document}
"""


def extract_title(text: str) -> str:
    match = re.search(r"\\title\{(?P<title>[^{}]+)\}", text)
    if not match:
        raise ValueError("missing title in anonymous source")
    return match.group("title")


def extract_body(text: str) -> str:
    marker = r"\begin{document}"
    if marker not in text:
        raise ValueError("missing begin{document} in anonymous source")
    body = text.split(marker, 1)[1]
    body = re.sub(r"^\s*\\maketitle\s*", "", body, count=1)
    body, count = re.subn(r"(\\end\{abstract\})", r"\1\n\n\\maketitle", body, count=1)
    if count != 1:
        raise ValueError("missing abstract block in anonymous source")
    body = body.replace(r"\bibliographystyle{plain}", r"\bibliographystyle{ACM-Reference-Format}")
    return body.lstrip()


def validate(text: str) -> list[str]:
    issues: list[str] = []
    required = (
        r"\documentclass[manuscript,screen,review,anonymous]{acmart}",
        r"\acmJournal{TQC}",
        r"\author{Anonymous Authors}",
        r"\bibliographystyle{ACM-Reference-Format}",
        r"\ccsdesc[500]{Theory of computation~Quantum computation theory}",
        r"\keywords{quantum Boolean oracle synthesis",
        r"\Description{Pipeline diagram",
        "anonymous review",
        "logical-layer",
    )
    for token in required:
        if token not in text:
            issues.append(f"missing token: {token}")
    forbidden = ("Zixiang Zhou", r"\documentclass[11pt]{article}", r"\usepackage[a4paper,margin=1in]{geometry}")
    for token in forbidden:
        if token in text:
            issues.append(f"forbidden token present: {token}")
    return issues


def main() -> int:
    source_text = ANONYMOUS_SOURCE.read_text(encoding="utf-8")
    title = extract_title(source_text)
    body = extract_body(source_text)
    output = ACM_PREAMBLE_TEMPLATE.replace("{title}", title) + body
    issues = validate(output)
    if issues:
        for issue in issues:
            print(f"error: {issue}", file=sys.stderr)
        return 1
    ACM_SOURCE.write_text(output, encoding="utf-8")
    print(f"wrote {ACM_SOURCE.relative_to(PROJECT_ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
